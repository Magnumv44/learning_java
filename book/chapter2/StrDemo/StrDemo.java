/*
 * Демонстрация управляющих последовательностей в символьных строках
 */
class StrDemo {
	public static void main(String[] args) {
		System.out.println("Первая строка\nВтороя строка"); // Использование последовательности \n переводит на новую строку
		System.out.println("A\tB\tC");                      // Использование последовательности \t делает табуляцию между символами
		System.out.println("D\tE\tF");
	}
}